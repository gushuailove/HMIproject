/*************************************************************
|1|描述 ：MCGSTouch发送函数   
|2|调用 ：无
|3|输入 ：
way:通道
	MCGSTouch.h -> Way类型 
		WAY_1 ：通道一
		WAY_2 ：通道二
type:动作类型
	MCGSTouch.h -> Type类型 
		DISPLAY_INTERFACE_1	：显示界面一
		DISPLAY_INTERFACE_2	：显示界面二
		DISPLAY_INTERFACE_3	：显示界面三
		DISPLAY_CONTINUED		：显示继续按钮
		DISPLAY_PAUSE				：显示暂停按钮		
		UPDATE_TIME					：更新剩余时间		
		READ_KEY						：读取按键状态					
		EMPTY_KEY						：清空按键状态		
		SET_MODE_TIME				：设置界面一中的模式和时间	
data1 : 存放工作模式
data2	：存放剩余时间/工作时间 
|4|返回 : 无
*************************************************************/
void MCGSTouch_Send(uint8_t way,uint8_t type,uint8_t data1,uint8_t data2)
{	
	switch(type)
	{
		case DISPLAY_INTERFACE_1:
		case DISPLAY_INTERFACE_2:
		case DISPLAY_INTERFACE_3:
			DisplayInterface(way,type,data2);
			break;
		case DISPLAY_CONTINUED:
		case DISPLAY_PAUSE:
			DisplayContinuedPause(way,type);
			break;
		case UPDATE_TIME:
			UpdateTime(way,data2);
			break;
		case READ_KEY:
			ReadKey(way);
			break;
		case EMPTY_KEY:
			EmptyKey(way);
			break;
		case SET_MODE_TIME:
			SetModeTime(way,data1,data2);
			break;
		default:
			return;
	}
}
/*************************************************************
|1|描述 ：MCGSTouch接收函数 
|2|调用 ：无
|3|输入 ：
type:动作类型
	MCGSTouch.h -> Type类型 
		DISPLAY_INTERFACE_1	：显示界面一
		DISPLAY_INTERFACE_2	：显示界面二
		DISPLAY_INTERFACE_3	：显示界面三
		DISPLAY_CONTINUED		：显示继续按钮
		DISPLAY_PAUSE				：显示暂停按钮		
		UPDATE_TIME					：更新剩余时间		
		READ_KEY						：读取按键状态					
		EMPTY_KEY						：清空按键状态		
		SET_MODE_TIME				：设置界面一中的模式和时间	
key  : 哪个按键被按下（只有询问按键时有用）
mode ：工作模式 
time ：工作时间 
|4|返回 : 2 ：没有按键被按下 3：有按键被按下 1：其它回复 0：无回复
*************************************************************/
uint8_t MCGSTouch_Receive(uint8_t type,uint8_t *key,uint8_t *mode,uint8_t *time)
{
	USATTBUF  bufRfid;
	uint8_t i = 3;
	
	if(get_buf_pt(&bufRfid))
	{
		switch(type)
		{
			case DISPLAY_INTERFACE_1:
			case DISPLAY_INTERFACE_2:
			case DISPLAY_INTERFACE_3:	
			case DISPLAY_CONTINUED:
			case DISPLAY_PAUSE:						
			case UPDATE_TIME:
			case EMPTY_KEY:
			case SET_MODE_TIME:
				if(bufRfid.array[0] == 0x01 &&
						bufRfid.array[1] == 0x10)
				{
					return 1;
				}	
				break;						
			case READ_KEY:
				if(bufRfid.array[0] == 0x01 &&
						bufRfid.array[1] == 0x03)
				{
					for(i=4; i<=12; i=i+2)
					{
						if(bufRfid.array[i])//继续//暂停//取消//确定//开始
						{
							*key = i;
							*mode = bufRfid.array[14];//模式
							*time = bufRfid.array[16];//时间
							return 3;
						}
					}	
					return 2;					
				}
				break;
			default:
				break;
		}
	}
	return 0;
}



