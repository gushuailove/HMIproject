uint16_t CRC16(uint8_t *puchMsg,uint8_t usDataLen) 	
{ 											
  uint8_t uchCRCHi = 0xFF ; 				
  uint8_t uchCRCLo = 0xFF ; 				
  uint16_t uIndex ; 							
  while (usDataLen--) 					
  {
    uIndex = uchCRCHi ^ *puchMsg++ ; 	
    uchCRCHi = uchCRCLo ^ CRCHi[uIndex] ;
    uchCRCLo = CRCLo[uIndex] ;
  }
  return (((uint16_t)(uchCRCLo) << 8) | uchCRCHi) ;
}


void send_message_handle(uint8_t *send_data,uint8_t send_size)
{ 
  uint8_t CRCHi = 0;
	uint8_t CRCLo = 0;
	
  uint16_t CRCData = 0;
	
	if(send_size)
	{
		CRCData = CRC16(send_data,send_size-2);
		CRCHi = CRCData & 0x00ff;
		CRCLo = CRCData >> 8;	
		*(send_data+send_size-1) = CRCLo;
		*(send_data+send_size-2) = CRCHi; 
		
		send_message(send_data,send_size);
	}  
}
